function moveSnow() {
  let start = 1450128121000;
  let end = 1451765616000;
  let len = end - start;
  let snowFlakes = 10;
  let step = len / snowFlakes;

  if (!snow) {
    snow = [];
    for (let i = 0; i < snowFlakes; i++) {
      snow.push({
        target: 'snow-' + i,
        datapoints: [[118, start + step * i], [117, start + step * i]],
      });
    }

    for (let i = 0; i < snowFlakes; i++) {
      snow.push({
        target: 'snow-' + i,
        datapoints: [[128, start + step * i], [127, start + step * i]],
      });
    }
  } else {
    for (let i = 0; i < snowFlakes * 2; i++) {
      let yMod = -Math.random();
      let xMod = (Math.random() - 0.5) * (step / 10);

      for (let dp of snow[i].datapoints) {
        dp[0] += yMod;
        dp[1] += xMod;
      }
    }
  }
}

